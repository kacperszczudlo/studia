fun sub(a: Int, b:Int):Int{
    return a - b
}
fun mul(a:Int, b:Int):Int{
    return a * b
}

fun min(a:Int, b: Int):Int{
    if (a>b){
        return b
    }else{
        return a
    }
}

fun add(a:Int, b: Int):Int{
    return a + b
}

fun div(a: Int, b: Int):Int{
    return a / b
}

fun max(a: Int, b: Int):Int{
    if (a>b){
        return a
    }else{
        return b
    }
}

