fun main() {
    var sum = 2 + 4
    var sub = 2 - 4
    var mul = 2 * 4
    var div = 2.0 / 4.0
    var mod = 2 % 4
    var digit = 2
    var result1 = 2 + 2 * 2
    var result2 = (2 + 2) * 2


